#include <iostream>
using namespace std;

int main() {
    int num = 5;
    double pi = 3.14159;
    char ch = 'A';
    bool isTrue = true;
    string name = "John";

    cout << "num = " << num << endl;
    cout << "pi = " << pi << endl;
    cout << "ch = " << ch << endl;
    cout << "isTrue = " << isTrue << endl;
    cout << "name = " << name << endl;
    
    return 0;
}